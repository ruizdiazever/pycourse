# Que es un fichero?
#   Un conjunto de bits almacenados en un dispositivo de memoria persistente,
#   indentificado con un nombre, una direccion a la ruta que lo contiene y
#   una extension.

# Que nos permite?
#   - Creacion, apertura, cierre, extension

# Que es el puntero del fichero?
#   Representa el lugar donde apunta el fichero antes de realizar una operacion
#   determinada, seria como el lugar donde ponemos nuestro dedo o fijamos la
#   vista mientras leemos o escribimos.

# Fichertos de texto y ficheros binarios
#   Los ficheros de texto guardan datos en forma de texto plano, son entendibles
#   por las personas, pero los binarios no, porque los guardan en el lenguaje de
#   la maquina.

